#? Typecasting in Python:
# The most important concept in python is typecasting, Typecasting means to convert one data type into another data type.

# For Example:
f = "5"; # String
#? print(f + 1); # This is a string and 1 is an integer, so it will give an error. We need to typecast it into an integer
# so we can do it like this:
print(int(f) + 1); # This is an integer now, and this is typecasting.

