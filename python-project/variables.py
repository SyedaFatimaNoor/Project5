# Variables in Python:
# Variables are containers for storing data values.

a = 5;
print(a);

b = 55
print(b);

a = 6.2;
print(a);

c = True;  # or False ---> These are boolean types of variables. 
print(c);

d = "Noor"; # String Variable ---> String is a sequence of characters like People Names, Places, etc.
print(d);

#! Everything in python is an object.

e = None;
print(e); # None is a special type of variable in Python which is used to define a null value. So, these are our basic built in data types of python.
#  We also have bytes, sets, dictonary, tuples, int, float, complex, etc.
